# this program is to illustrate the range function use in for loop

# range function with one argument: specifies the loop to be executed  till

print('range function with one argument:')
for i in range(5):
    print(i)

# range function with two arguments: specifies start and end of the loop

print('range function with two argument:')
for i in range(0,10):
    print(i)

# range function with three arguments: specifies start end and step

print('range function with three argument:')
for i in range(10 , 20 , 2):
    print(i)
